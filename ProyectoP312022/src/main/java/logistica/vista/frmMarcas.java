    public void llenadoDeCombos() {
        daoMarcas marcasDAO = new daoMarcas();
        List<clsMarcas> marcas;
        marcas = marcasDAO.select();
        cbox_marcas.addItem("Seleccione una opci√≥n");
        for (int i = 0; i < marcas.size(); i++) {
            cbox_marcas.addItem(marcas.get(i).getMarnombre());
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Marca");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estado");
        daoMarcas marcasDAO = new daoMarcas();
        List<clsMarcas> marcas = marcasDAO.select();
        tablaMarcas.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < marcas.size(); i++) {
            dato[0] = Integer.toString(marcas.get(i).getMarid());
            dato[1] = marcas.get(i).getMarnombre();
            dato[2] = marcas.get(i).getMarstatus();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscaraplicacion() {
        clsMarcas marcasAConsultar = new clsMarcas();
        daoMarcas marcasDAO = new daoMarcas();
        marcasAConsultar.setMarid(Integer.parseInt(txtbuscado.getText()));
        marcasAConsultar = (clsMarcas) marcasDAO.query(marcasAConsultar);
        txtNombre.setText(marcasAConsultar.getMarnombre());
        txtEstado.setText(marcasAConsultar.getMarstatus());
    }

